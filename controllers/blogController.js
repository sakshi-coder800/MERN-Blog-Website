import mongoose  from  "mongoose"
import blogModel from "../model/blogModel.js";
import userModel from "../model/userModel.js";

//GET ALL BLOGS
export const getAllBlogsController = async (req, res) => {
  try {
    const blogs = await blogModel.find({}).populate("user");
    if (!blogs) {
      return res.status(200).send({// request ok but no blog found
        success: false,
        message: "No Blogs Found",
      });
    }
    return res.status(200).send({
      success: true,
      BlogCount: blogs.length,
      message: "All Blogs lists",
      blogs,
    });
  } catch (error) {
    console.log(error);
    return res.status(500).send({
      success: false,
      message: "Error WHile Getting Blogs",
      error,
    });
  }
};

// Create Blog
export const createBlogController = async (req, res) => {
  try {
    const { title, description, image, user } = req.body;

    // Validation 
    if (!title || !description || !image || !user) {
      return res.status(400).send({
        success: false,
        message: "Please Provide All Fields",
      });
    }

// existing users // Check if user exists 
    const existingUser = await userModel.findById(user);
    if (!existingUser) {
      return res.status(404).send({
        success: false,
        message: "Unable to find user",
      });
    }

    // Create blog
    const newBlog = new blogModel({ title, description, image, user });
    
    // Start transaction
    const session = await mongoose.startSession();// create session 
    session.startTransaction(); // perform transaction

    // Save new blog and update user's blogs
    await newBlog.save({ session });
    existingUser.blogs = existingUser.blogs || []; // Initialize if not already defined
    existingUser.blogs.push(newBlog);
    await existingUser.save({ session });

    // Commit transaction
    await session.commitTransaction();

    return res.status(201).send({
      success: true,
      message: "Blog Created!",
      newBlog,
    });
  } catch (error) {
    console.log(error);
    return res.status(400).send({
      success: false,
      message: "Error while creating blog",
      error,
    });
  }
};


